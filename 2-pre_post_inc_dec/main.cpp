#include <iostream>

using namespace std;

int main(){
	
	int num = 47;
	
	//post increment and pre increment
	
	num++;
	cout<<num<<endl;
	++num;
	cout<<num<<endl;
	num = num + 1;
	cout<<num<<endl<<endl;
	
	//post decrement and pre decrement
	
	num--;
	cout<<num<<endl;
	--num;
	cout<<num<<endl;
	num = num -1;
	cout<<num<<endl;
	return 0;
}
